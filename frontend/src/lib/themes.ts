export type ThemeId = (typeof APP_THEMES)[number]["id"];

const THEME_MAP = new Map(APP_THEMES.map((theme) => [theme.id, theme]));

export function getThemeById(themeId?: string | null): AppTheme {
  return THEME_MAP.get(themeId ?? "") ?? THEME_MAP.get(DEFAULT_THEME_ID)!;
}

export function getStoredThemeId(): ThemeId {
  if (typeof window === "undefined") {
    return DEFAULT_THEME_ID;
  }

  try {
    const storedThemeId = window.localStorage.getItem(THEME_STORAGE_KEY);
    return getThemeById(storedThemeId).id as ThemeId;
  } catch {
    return DEFAULT_THEME_ID;
  }
}

export function persistThemeId(themeId: ThemeId) {
  if (typeof window === "undefined") {
    return;
  }

  try {
    window.localStorage.setItem(THEME_STORAGE_KEY, themeId);
  } catch {
    return;
  }
}

export function applyTheme(themeId: ThemeId) {
  if (typeof document === "undefined") {
    return;
  }

  const theme = getThemeById(themeId);
  const root = document.documentElement;

  root.dataset.theme = theme.id;
  root.dataset.themeMode = theme.mode;
  root.style.colorScheme = theme.mode;

  Object.entries(theme.vars).forEach(([key, value]) => {
    root.style.setProperty(key, value);
  });
}

export function applyStoredTheme() {
  applyTheme(getStoredThemeId());
}

